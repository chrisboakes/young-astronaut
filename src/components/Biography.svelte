    <p>
        Young Astronaut were formed in 2012 by brothers Chris &amp; Pete Boakes. Since then they’ve toured across the UK covering almost the entirety of London’s backroom venues (Camden Assembly, The Hoxton Square Bar &amp; Kitchen, The Garage, Dublin Castle, etc.) and internationally in Beijing, Chengdu and Hong Kong. Their previous two full length albums, folk driven 'Fawn' and rockier 'A Gold Frontier', earned the band national radio play on BBC 6 Music as well as being featured on the BBC introducing podcast.
    </p>
    <p>
        Young Astronaut's forthcoming 2022 EP "Pirouette &amp; Fall" is punchy and melodic. The first single "Hope" is set to a mesmerising backdrop of metronomic synthesisers, drums and exposed guitars (akin to that of Future Islands). It talks about accepting failure and moving on while battling with feelings of apprehension and ambivalence - themes that are internalised on rockier "Hellions" and challenged on orchestral "The Room Beneath My Skin". Pirouette &amp; Fall showcases the band's ability to draw together a distinguished soundscape with intimate lyrics and brooding vocals.
    </p>
